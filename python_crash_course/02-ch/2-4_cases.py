"""
2-4. Name Cases

Use a variable to represent a person's name, and then
print that person's name in lowercase, uppercase, and title case.
"""

# define a variable & assign a string
name = 'aMiR sAdIk'

# print the name in lowecase
print(name.lower())

#print the name in all uppercase
print(name.upper())

# print the name in title case (capitalise the first letter of each word)
print(name.title())
